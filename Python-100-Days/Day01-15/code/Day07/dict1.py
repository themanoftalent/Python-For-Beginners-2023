Dict = {}
print("Empty Dictionary: ")
print(Dict)
print('\n')

# Creating a Dictionary
# with Integer Keys
Dict = {1: 'One', 'Two': 'we can say 2', 'w/o quotation mark': 234}
print("\nDictionary Keys: ")
print(Dict)

# Creating a Dictionary
# with Mixed keys
Dict = {'Name': 'Mememt', 1: [1, 2, 3, 4]}
print("\nDictionary with the use of Mixed Keys: ")
print(Dict)

# Creating a Dictionary
# with dict() method
Dict = dict({'when we create with method': 'just', 2: 'Second method', 'You can see': [2,3,4,5,5,7]})
print("\nDictionary with the use of dict(): ")
print(Dict)

# Creating a Dictionary
# with each item as a Pair
Dict = dict([('name','Fatih'),('surname','No colon')])
print("\nDictionary with each item as a pair: ")
print(Dict)

